arr = []
n = int(input("Enter list size: "))
for i in range (n):
    arr.append(int(input("Enter number: ")))
print(*arr, sep=' ')
for i in range(1,n,1):
    a = arr[i]
    for j in range (i-1,-1,-1):
        b = arr[j]
        if a < arr[j]:
            arr[j] = a
            arr[j+1] = b
            print(*arr, sep=' ')

             


































































































































































































